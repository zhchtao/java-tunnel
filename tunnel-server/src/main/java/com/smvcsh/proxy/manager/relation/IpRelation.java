package com.smvcsh.proxy.manager.relation;

public class IpRelation {

	private int port;
	private String remotHost;
	private int remotPort;
	
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public String getRemotHost() {
		return remotHost;
	}
	public void setRemotHost(String remotHost) {
		this.remotHost = remotHost;
	}
	public int getRemotPort() {
		return remotPort;
	}
	public void setRemotPort(int remotPort) {
		this.remotPort = remotPort;
	}
	
	
}
